use crate::types::{Kit, Pattern, Velocity, NUM_STEPS, NUM_TRACKS};

fn mk(name: &str, kit: Kit, tempo: u16, swing: u8, raw: [[u8; NUM_STEPS]; NUM_TRACKS]) -> Pattern {
    Pattern {
        steps: raw.map(|row| row.map(Velocity::from_u8)),
        name: name.to_string(),
        kit,
        tempo,
        swing,
        ..Pattern::default()
    }
}

